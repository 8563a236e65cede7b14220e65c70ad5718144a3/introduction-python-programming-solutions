'''
Perform a specific task to calculate a value and return
that value
    return statement
    return [expression_list]

If an expression list is present, it is evaluated
    Otherwise None is substituted

Terminates the execution of the function definition
in which it appears and returns control to the calling
function
Can also return optional value to calling function

Functions without a return statement are called void
functions and return None

Functions can return a list or tuple

You can return multiple values separated by a comma which
by default is returned as a tuple by Python

You can assign the result to multiple variables that are
returned by the function definition - called tuple unpacking
'''